public class Request {
    int reqId;
    String name;
    public int getReqId() {
        return reqId;
    }
    public void setReqId(int reqId) {
        this.reqId = reqId;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
}
